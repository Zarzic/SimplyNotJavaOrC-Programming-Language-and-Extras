package Entity;

/**
 * This class represents the core functionality of the reminder application, which includes managing reminder data, 
 * such as creating new reminders, updating existing ones, and deleting reminders
 * @author rschi
 */
public class Reminder {
    private String title;
    private Date date;

    public Reminder(String title, Date date) {
        this.title = title;
        this.date = date;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }
}
